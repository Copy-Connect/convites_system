function theme(
  config: Omit<SuperheroTheme, 'copy' | 'desktopBackgroundUrl'> & {
    copy?: Partial<SuperheroTheme['copy']>;
  },
): SuperheroTheme {
  return {
    ...config,
    desktopBackgroundUrl: desktopBackgrounds[config.slug],
    copy: { ...commonCopy, ...config.copy },
  };
}

